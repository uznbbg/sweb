/**
 * One-time generator for placeholder .webp images so the layout has correct
 * dimensions and aspect ratios while you wait on real assets.
 *
 *   node scripts/generate-placeholders.mjs
 *
 * Each output is a solid Apple-palette tone. Real photos should overwrite
 * these files at the same paths.
 */
import sharp from 'sharp'
import { mkdir, writeFile } from 'node:fs/promises'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const OUT = join(__dirname, '..', 'public', 'images')

// Apple-palette tones (R,G,B,A)
const OFFWHITE = { r: 0xfb, g: 0xfb, b: 0xfd, alpha: 1 }
const LIGHTGRAY = { r: 0xf5, g: 0xf5, b: 0xf7, alpha: 1 }
const BLACK = { r: 0x1d, g: 0x1d, b: 0x1f, alpha: 1 }

const placeholders = [
  { file: 'hero.webp', w: 2400, h: 1400, bg: OFFWHITE },
  { file: 'og.webp', w: 1200, h: 630, bg: OFFWHITE },
  { file: 'saglik.webp', w: 1200, h: 1200, bg: LIGHTGRAY },
  { file: 'kasko.webp', w: 1200, h: 1200, bg: LIGHTGRAY },
  { file: 'konut.webp', w: 1200, h: 1200, bg: LIGHTGRAY },
  { file: 'hayat.webp', w: 1200, h: 1200, bg: LIGHTGRAY },
  { file: 'spotlight-a.webp', w: 2400, h: 1400, bg: BLACK },
  { file: 'spotlight-b.webp', w: 1200, h: 1500, bg: OFFWHITE },
  { file: 'spotlight-c.webp', w: 2400, h: 1400, bg: BLACK },
  { file: 'saglik-hero.webp', w: 2400, h: 1400, bg: OFFWHITE },
  { file: 'saglik-feature.webp', w: 1200, h: 1500, bg: OFFWHITE },
  { file: 'kurumsal-hero.webp', w: 2400, h: 1400, bg: OFFWHITE },
  { file: 'kurumsal-feature.webp', w: 1200, h: 1500, bg: LIGHTGRAY },
  { file: 'hasar-hero.webp', w: 2400, h: 1400, bg: OFFWHITE },
  { file: 'destek-hero.webp', w: 2400, h: 1400, bg: OFFWHITE },
  { file: 'hakkimizda-hero.webp', w: 2400, h: 1400, bg: OFFWHITE },
  { file: 'hakkimizda-story.webp', w: 1200, h: 1500, bg: LIGHTGRAY },
]

await mkdir(OUT, { recursive: true })

const blurEntries = []

for (const p of placeholders) {
  const out = join(OUT, p.file)
  await sharp({
    create: { width: p.w, height: p.h, channels: 4, background: p.bg },
  })
    .webp({ quality: 80 })
    .toFile(out)

  // Tiny 8-wide JPEG for blurDataURL.
  const blurBuf = await sharp({
    create: { width: 8, height: 8, channels: 4, background: p.bg },
  })
    .jpeg({ quality: 30 })
    .toBuffer()
  const blurDataURL = `data:image/jpeg;base64,${blurBuf.toString('base64')}`
  const key = p.file
    .replace(/\.webp$/, '')
    .replace(/[^a-zA-Z0-9]/g, '_')
    .toUpperCase()
  blurEntries.push({ key, value: blurDataURL, w: p.w, h: p.h })

  console.log(`✓ ${p.file}  (${p.w}×${p.h})`)
}

const tsBody =
  `/* AUTO-GENERATED by scripts/generate-placeholders.mjs — do not edit. */\n\n` +
  `export const IMAGE_META = {\n` +
  blurEntries
    .map(
      (e) =>
        `  ${e.key}: { width: ${e.w}, height: ${e.h}, blurDataURL: '${e.value}' },`
    )
    .join('\n') +
  `\n} as const\n`

await writeFile(join(__dirname, '..', 'lib', 'image-meta.ts'), tsBody, 'utf8')

console.log(
  `\nWrote ${placeholders.length} placeholders to public/images/ and lib/image-meta.ts`
)
